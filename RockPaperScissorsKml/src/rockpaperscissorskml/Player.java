
package gricoapp;


 public class Player {

    
   
   public  String Name;
 public  int Stage,Score,Janken,Rank;
 
    

    public Player() {

    }

    /**
     * @return the Name
     */
    public String getName() {
        return Name;
    }

    /**
     * @param Name the Name to set
     */
    public void setName(String Name) {
        this.Name = Name;
    }

    /**
     * @return the Stage
     */
    public int getStage() {
        return Stage;
    }

    /**
     * @param Stage the Stage to set
     */
    public void setStage(int Stage) {
        this.Stage = Stage;
    }

    /**
     * @return the Score
     */
    public int getScore() {
        return Score;
    }

    /**
     * @param Score the Score to set
     */
    public void setScore(int Score) {
        this.Score = Score;
    }

    /**
     * @return the Janken
     */
    public int getJanken() {
        return Janken;
    }

    /**
     * @param Janken the Janken to set
     */
    public void setJanken(int Janken) {
        this.Janken = Janken;
    }

    /**
     * @return the Rank
     */
    public int getRank() {
        return Rank;
    }

    public void setRank(int Rank) {
        this.Rank = Rank;
    }
 }
 
